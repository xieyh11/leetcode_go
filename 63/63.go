func nChooseK(n, k int) [][]bool {
	if n < k || n == 0 {
		return [][]bool{}
	}
	if k == 0 {
		return [][]bool{make([]bool, n)}
	}
	if n == k {
		res := make([]bool, n)
		for i := range res {
			res[i] = true
		}
		return [][]bool{res}
	}
	if k == 1 {
		res := make([][]bool, n)
		for i := range res {
			res[i] = make([]bool, n)
			res[i][i] = true
		}
		return res
	}
	half := n / 2
	remain := n - n/2
	res := make([][]bool, 0)
	for i := 0; i <= k && i <= half; i++ {
		left := nChooseK(half, i)
		right := nChooseK(remain, k-i)
		for il := range left {
			for ir := range right {
				temp := make([]bool, n)
				copy(temp[:half], left[il])
				copy(temp[half:], right[ir])
				res = append(res, temp)
			}
		}
	}
	return res
}

func uniquePathsWithObstacles(obstacleGrid [][]int) int {
	if len(obstacleGrid) == 0 || len(obstacleGrid[0]) == 0 {
		return 0
	}
	rows := len(obstacleGrid)
	cols := len(obstacleGrid[0])
	if obstacleGrid[rows-1][cols-1] == 1 || obstacleGrid[0][0] == 1 {
		return 0
	}
	if rows == 1 && cols == 1 {
		return 1
	}
	choice := nChooseK(rows-1+cols-1, rows-1)
	count := 0
	for _, ch := range choice {
		r, c := 0, 0
		finished := true
		for _, one := range ch {
			if one {
				r++
			} else {
				c++
			}
			if obstacleGrid[r][c] == 1 {
				finished = false
				break
			}
		}
		if finished {
			count++
		}
	}
	return count
}

